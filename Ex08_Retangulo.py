# Exercício de cálculo da área de um retângulo

print("Vamos ler as dimensões do retângulo e exibir a sua área em m²")

base = float(input("Digite a base do retângulo em m²: "))
altura = float(input("Digite a altura do retângulo em m²: "))

area = base * altura

print("A área do retângulo é:", area,"m²")